package autoPackage;

public class Resumen {

	private int max;
	private int min;
	private double promedio;
	
	public Resumen(int max, int min, double promedio) {
		super();
		this.max = max;
		this.min = min;
		this.promedio = promedio;
	}

	public int getMax() {
		return max;
	}

	public int getMin() {
		return min;
	}

	public double getPromedio() {
		return promedio;
	}
	
}
